public class IntroTask3Digits {
    public static void main (String[] args){
        int a = -999;
        if (a >= 100 & a <= 999) {
            System.out.println("true");
        } else if ( a <= -100 & a >= -999){
            System.out.println("true");
        }else {
            System.out.println("false");
        }
    }
}
